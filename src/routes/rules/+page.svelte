    <h3>§1 ACTIVE TASKS</h3>
    <p>When working on your active task and other activities…</p>

    <p>a) You are not allowed to work towards uncompleted (active) tasks, if it can be avoided.</p>

    <p>b) Avoid gaining experience in skills unless it’s necessary to progress your task.</p>

    <p>c) Do not go out of your way to obtain items other than those that benefit your active task.</p>

    <p>d) You are allowed to (re)complete previously fully completed tiers at will, when new tasks are added.</p>

    <p>e) You are allowed to buy skill mastery capes (and mark their tasks as complete) as soon as you achieve level 99 or 120 in a skill.</p>

    <p>f) Utilizing boosting services through any means is strictly prohibited.</p>
